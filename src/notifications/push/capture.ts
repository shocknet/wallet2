import { Capacitor } from "@capacitor/core";
import { PushNotifications } from "@capacitor/push-notifications";
import {
	PushNotificationEnvelope,
	PushNotificationEnvelopeValidate,
} from "@/Api/pub/autogenerated/ts/types";
import dLogger from "@/Api/helpers/debugLog";
import store from "@/State/store/store";
import { setIdentityIntent } from "@/identityIntent/identityActivationBus";

const log = dLogger.withContext({ component: "push-capture" });

export function captureWebEarly() {
	const u = new URL(window.location.href);
	if (hasPushParams(u)) {
		const envelope = parsePushEnvelopeFromUrl(u);
		u.searchParams.delete("push");
		u.searchParams.delete("push_envelope");
		history.replaceState({}, "", u.toString());
		if (envelope) {
			void processEnvelope(envelope, "web-cold-start");
		}
	}

	if ("serviceWorker" in navigator) {
		navigator.serviceWorker.addEventListener("message", (ev) => {
			const parsedEnvelope = parsePushEnvelopeFromPayload(ev.data);
			if (parsedEnvelope) {
				void processEnvelope(parsedEnvelope, "web-warm-start");
			}
		});
	}
}

export function captureNativeEarly() {
	const platform = Capacitor.getPlatform();
	if (platform === "web") return;

	PushNotifications.addListener("pushNotificationActionPerformed", (action) => {
		const d: any = action.notification?.data;
		const rawEnvelope = typeof d?.raw === "string"
			? d.raw
			: typeof d === "string"
				? d
				: null;

		if (!rawEnvelope) {
			log.warn("native_no_raw_envelope", { data: { hasData: !!d } });
			return;
		}

		const parsedEnvelope = parseEnvelopeJsonString(rawEnvelope);
		if (parsedEnvelope) {
			void processEnvelope(parsedEnvelope, `native-${platform}`);
		}
	});
}




export async function resolveTopicTarget(
	topicId: string,
	state = store.getState()
) {
	if (!topicId) {
		log.warn("resolve_missing_topic_id", {});
		return null;
	}

	const topicIndex = state.identitiesRegistry.topicIndexById[topicId];
	if (!topicIndex) return null;
	return {
		identityId: topicIndex.identityId,
		sourceId: topicIndex.sourceId,
	};
}

export function parseEnvelopeJsonString(value: string): PushNotificationEnvelope | null {
	try {
		const candidate = JSON.parse(value);
		const err = PushNotificationEnvelopeValidate(candidate);
		if (err) return null;
		return candidate as PushNotificationEnvelope;
	} catch {
		return null;
	}
}

export function parsePushEnvelopeFromPayload(payload: any): PushNotificationEnvelope | null {
	const err = PushNotificationEnvelopeValidate(payload);
	if (err) return null;
	return payload as PushNotificationEnvelope;
}

export function hasPushParams(url: URL): boolean {
	return url.searchParams.get("push") === "1";
}

export function parsePushEnvelopeFromUrl(url: URL): PushNotificationEnvelope | null {
	if (!hasPushParams(url)) return null;
	const rawEnvelope = url.searchParams.get("push_envelope");
	if (!rawEnvelope) return null;
	let decoded = rawEnvelope;
	try {
		decoded = decodeURIComponent(rawEnvelope);
	} catch {
		decoded = rawEnvelope;
	}
	return parseEnvelopeJsonString(decoded);
}



async function processEnvelope(envelope: PushNotificationEnvelope, source: string) {
	log.debug("envelope_received", { data: { source } });
	const resolution = await resolveTopicTarget(envelope.topic_id);
	if (!resolution) {
		log.warn("topic_resolve_failed", { data: { source } });
		return;
	}
	setIdentityIntent({
		createdAtMs: Date.now(),
		identityId: resolution.identityId,
		push: {
			topicId: envelope.topic_id,
			sourceId: resolution.sourceId,
			envelope,
		},
	});
	log.info("pending_push_stage_set", { data: { source, identityId: resolution.identityId } });
}

