function formatSats(n: number): string {
    return n.toLocaleString() + " sats";
}

function formatTableTs(unix: number): string {
    if (!unix) return "—";
    const d = new Date(unix * 1000);
    const pad = (n: number) => String(n).padStart(2, "0");
    const day = pad(d.getDate());
    const month = pad(d.getMonth() + 1);
    const year = String(d.getFullYear()).slice(-2);
    const hours = pad(d.getHours());
    const minutes = pad(d.getMinutes());
    const seconds = pad(d.getSeconds());
    return `${day}/${month}/${year} ${hours}:${minutes}:${seconds}`;
}

function formatTableAmount(n: number): string {
    return n.toLocaleString("fr-FR");
}

function trackedTypeLabel(type: TrackedOperationType): string {
    return type === TrackedOperationType.ROOT ? "ROOT" : "USER";
}

const TIME_MISMATCH_LEEWAY_S = 10;

function timestampsMismatch(a: number, b: number): boolean {
    return Math.abs(a - b) > TIME_MISMATCH_LEEWAY_S;
}

function getMismatch(op: AssetOperation): { amount: boolean; ts: boolean } | null {
    if (!op.tracked) return null;
    const amount = op.amount !== op.tracked.amount;
    const ts = timestampsMismatch(op.ts, op.tracked.ts);
    if (!amount && !ts) return null;
    return { amount, ts };
}

function mismatchNotes(mismatch: { amount: boolean; ts: boolean } | null): string {
    if (!mismatch) return "";
    if (mismatch.amount && mismatch.ts) return "amount + time mismatch";
    if (mismatch.amount) return "amount mismatch";
    return "time mismatch";
}

type ProviderKind = "lnd" | "lp";
type AssetOpKind = LndOpType | LpOpType;

type UnifiedAssetOperation = {
    op: AssetOperation;
    providerKind: ProviderKind;
    providerPubkey: string;
    opKind: AssetOpKind;
};

function collectAllOperations(data: AssetsAndLiabilities): UnifiedAssetOperation[] {
    const rows: UnifiedAssetOperation[] = [];

    for (const lnd of data.lnds) {
        if (!lnd.tracked) continue;
        const { tracked } = lnd;
        for (const op of tracked.invoices) rows.push({ op, providerKind: "lnd", providerPubkey: lnd.pubkey, opKind: "invoice" });
        for (const op of tracked.payments) rows.push({ op, providerKind: "lnd", providerPubkey: lnd.pubkey, opKind: "payment" });
        for (const op of tracked.incoming_tx) rows.push({ op, providerKind: "lnd", providerPubkey: lnd.pubkey, opKind: "incoming_tx" });
        for (const op of tracked.outgoing_tx) rows.push({ op, providerKind: "lnd", providerPubkey: lnd.pubkey, opKind: "outgoing_tx" });
    }

    for (const lp of data.liquidity_providers) {
        if (!lp.tracked) continue;
        const { tracked } = lp;
        for (const op of tracked.invoices) rows.push({ op, providerKind: "lp", providerPubkey: lp.pubkey, opKind: "invoice" });
        for (const op of tracked.payments) rows.push({ op, providerKind: "lp", providerPubkey: lp.pubkey, opKind: "payment" });
    }

    return rows.sort((a, b) => b.op.ts - a.op.ts);
}

function txTypeLabel(op: AssetOperation): string {
    if (!op.tracked) return "—";
    if (op.tracked.type === TrackedOperationType.USER && op.tracked.user_id) {
        return op.tracked.user_id;
    }
    return trackedTypeLabel(op.tracked.type);
}

function trackedIconTitle(op: AssetOperation): string {
    if (!op.tracked) return "Untracked";
    const mismatch = getMismatch(op);
    if (mismatch) return mismatchNotes(mismatch);
    return "Tracked";
}

function opKindLabel(kind: AssetOpKind): string {
    if (kind === "invoice") return "Invoice";
    if (kind === "payment") return "Payment";
    if (kind === "incoming_tx") return "Incoming tx";
    return "Outgoing tx";
}

function providerLabel(kind: ProviderKind): string {
    return kind === "lnd" ? "LND" : "LP";
}

function providerIcon(kind: ProviderKind) {
    return kind === "lnd" ? flashOutline : linkOutline;
}

