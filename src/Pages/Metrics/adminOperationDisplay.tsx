import { IonIcon } from "@ionic/react";
import { flashOutline, linkOutline, personOutline } from "ionicons/icons";
import { UserOperation, UserOperationType } from "@/Api/pub/autogenerated/ts/types";
import {
    formatTableAmount,
    formatTableTs,
    MetricsDataTable,
    MetricsTableEmpty,
    MetricsTableRow,
} from "./metricsDataTable";

const OPERATIONS_GRID = "minmax(100px, 1.2fr) minmax(110px, 1.2fr) minmax(72px, 90px) minmax(72px, 90px)";

export function operationIconType(t: UserOperationType) {
    switch (t) {
        case UserOperationType.INCOMING_INVOICE:
        case UserOperationType.OUTGOING_INVOICE:
            return flashOutline;
        case UserOperationType.INCOMING_USER_TO_USER:
        case UserOperationType.OUTGOING_USER_TO_USER:
            return personOutline;
        case UserOperationType.INCOMING_TX:
        case UserOperationType.OUTGOING_TX:
            return linkOutline;
    }
}

export function operationIconColor(t: UserOperationType) {
    switch (t) {
        case UserOperationType.INCOMING_INVOICE:
        case UserOperationType.INCOMING_USER_TO_USER:
        case UserOperationType.INCOMING_TX:
            return "var(--ion-color-success)";
        case UserOperationType.OUTGOING_INVOICE:
        case UserOperationType.OUTGOING_USER_TO_USER:
        case UserOperationType.OUTGOING_TX:
            return "var(--ion-color-danger)";
        default:
            return "var(--ion-color-medium)";
    }
}

function operationTypeLabel(t: UserOperationType): string {
    switch (t) {
        case UserOperationType.INCOMING_INVOICE:
            return "Incoming invoice";
        case UserOperationType.OUTGOING_INVOICE:
            return "Outgoing invoice";
        case UserOperationType.INCOMING_USER_TO_USER:
            return "Incoming U2U";
        case UserOperationType.OUTGOING_USER_TO_USER:
            return "Outgoing U2U";
        case UserOperationType.INCOMING_TX:
            return "Incoming tx";
        case UserOperationType.OUTGOING_TX:
            return "Outgoing tx";
    }
}

export function AdminOperationsList({ operations }: { operations: UserOperation[] }) {
    const sorted = [...operations].sort((a, b) => b.paidAtUnix - a.paidAtUnix);

    if (sorted.length === 0) {
        return <MetricsTableEmpty message="No operations" />;
    }

    return (
        <MetricsDataTable
            grid={OPERATIONS_GRID}
            header={
                <>
                    <span>Op type</span>
                    <span>Date</span>
                    <span style={{ textAlign: "right" }}>Amount</span>
                    <span style={{ textAlign: "right" }}>Service fee</span>
                </>
            }
        >
            {sorted.map((op, i) => (
                <MetricsTableRow key={op.operationId} grid={OPERATIONS_GRID} isLast={i === sorted.length - 1}>
                    <div style={{ display: "flex", alignItems: "center", gap: "6px", minWidth: 0 }}>
                        <IonIcon
                            icon={operationIconType(op.type)}
                            style={{ flexShrink: 0, color: operationIconColor(op.type), fontSize: "1rem" }}
                            title={operationTypeLabel(op.type)}
                        />
                        <span style={{ overflow: "hidden", textOverflow: "ellipsis", whiteSpace: "nowrap" }}>
                            {operationTypeLabel(op.type)}
                        </span>
                    </div>
                    <span style={{ fontSize: "0.8rem", color: "var(--ion-color-medium)" }}>
                        {formatTableTs(op.paidAtUnix)}
                    </span>
                    <span style={{ textAlign: "right", fontVariantNumeric: "tabular-nums" }}>
                        {formatTableAmount(op.amount)}
                    </span>
                    <span style={{ textAlign: "right", fontVariantNumeric: "tabular-nums" }}>
                        {formatTableAmount(op.service_fee)}
                    </span>
                </MetricsTableRow>
            ))}
        </MetricsDataTable>
    );
}
