import { useCallback, useEffect, useMemo, useState } from "react";
import { RouteComponentProps } from "react-router-dom";
import {
    IonBadge,
    IonButton,
    IonCard,
    IonCardContent,
    IonCardHeader,
    IonCardTitle,
    IonContent,
    IonHeader,
    IonPage,
    IonText,
    useIonLoading,
    useIonRouter,
} from "@ionic/react";
import { toast } from "react-toastify";
import { UserAdminInfo, UsersAdminInfo } from "@/Api/pub/autogenerated/ts/types";
import MetricsSubPageToolbar from "@/Layout2/Metrics/MetricsSubPageToolbar";
import { useAppSelector } from "@/State/store/hooks";
import { selectAdminNprofileViews } from "@/State/scoped/backups/sources/selectors";
import { selectSelectedMetricsAdminSourceId } from "@/State/runtime/slice";
import { fetcher } from "./fetcher";
import { formatTableAmount, MetricsDataTable, MetricsTableEmpty, MetricsTableRow } from "./metricsDataTable";

const PAGE_SIZE = 50;
const USERS_GRID = "minmax(100px, 1.2fr) minmax(90px, 1fr) minmax(120px, 1.5fr) 68px";

function trimId(id: string): string {
    return id.length <= 12 ? id : `${id.slice(0, 6)}…${id.slice(-4)}`;
}

export default function UsersAdmin({ history }: RouteComponentProps) {
    const router = useIonRouter();
    const admins = useAppSelector(selectAdminNprofileViews);
    const selectedId = useAppSelector(selectSelectedMetricsAdminSourceId);
    const adminSource = useMemo(
        () => admins.find((a) => a.sourceId === selectedId),
        [admins, selectedId]
    );

    const [page, setPage] = useState(0);
    const [data, setData] = useState<UsersAdminInfo>({ total: 0, users: [] });
    const [loading, setLoading] = useState(true);
    const [error, setError] = useState<string | null>(null);
    const [presentLoading, dismissLoading] = useIonLoading();

    const skip = page * PAGE_SIZE;
    const rangeStart = data.total === 0 ? 0 : skip + 1;
    const rangeEnd = Math.min(skip + data.users.length, data.total);
    const hasPrev = page > 0;
    const hasNext = skip + data.users.length < data.total;

    const fetchUsers = useCallback(async () => {
        if (!adminSource) return;

        const res = await fetcher(
            { pubkey: adminSource.lpk, relays: adminSource.relays },
            adminSource.keys,
            {
                onStart: async () => {
                    setError(null);
                    setLoading(true);
                    await dismissLoading();
                    await presentLoading("Fetching users...");
                },
                onEnd: async () => {
                    setLoading(false);
                    await dismissLoading();
                },
                onFail: (err) => {
                    setError(err);
                    toast.error(err);
                },
            },
            [(client) => client.GetUsersAdminInfo({ skip, take: PAGE_SIZE })]
        );

        if (res) {
            setData(res[0]);
        }
    }, [adminSource, dismissLoading, presentLoading, skip]);

    useEffect(() => {
        void fetchUsers();
    }, [fetchUsers]);

    const openUser = (user: UserAdminInfo) => {
        history.push(`/metrics/users/${user.user_id}`, { user });
    };

    return (
        <IonPage className="ion-page-width">
            <IonHeader className="ion-no-border">
                <MetricsSubPageToolbar title="Users" />
            </IonHeader>

            <IonContent className="ion-padding ion-content-no-footer">
                {error && (
                    <div style={{ color: "red", padding: 12 }}>
                        <div style={{ fontWeight: 700, marginBottom: 8 }}>Something went wrong</div>
                        <div style={{ opacity: 0.85, marginBottom: 12 }}>{error}</div>
                        <div style={{ display: "flex", gap: 8 }}>
                            <IonButton onClick={() => void fetchUsers()}>Retry</IonButton>
                            <IonButton fill="outline" onClick={() => router.push("/metrics/select", "back")}>
                                Change Source
                            </IonButton>
                        </div>
                    </div>
                )}

                {!error && (
                    <>
                        <IonCard className="ion-margin-top">
                            <IonCardHeader>
                                <IonCardTitle>
                                    Users
                                    {data.total > 0 && (
                                        <IonText color="medium" style={{ fontSize: "0.85rem", fontWeight: 400, marginLeft: "8px" }}>
                                            ({data.total.toLocaleString()})
                                        </IonText>
                                    )}
                                </IonCardTitle>
                            </IonCardHeader>
                            <IonCardContent>
                                <IonText color="medium" style={{ fontSize: "0.8rem", display: "block", marginBottom: "12px" }}>
                                    {data.total === 0 && !loading
                                        ? "No users"
                                        : `Showing ${rangeStart}–${rangeEnd} of ${data.total.toLocaleString()}`}
                                </IonText>

                                {data.users.length === 0 && !loading ? (
                                    <MetricsTableEmpty message="No users" />
                                ) : (
                                    <MetricsDataTable
                                        grid={USERS_GRID}
                                        minWidth="420px"
                                        header={
                                            <>
                                                <span>User</span>
                                                <span style={{ textAlign: "right" }}>Balance</span>
                                                <span>Details</span>
                                                <span style={{ textAlign: "center" }}>Status</span>
                                            </>
                                        }
                                    >
                                        {data.users.map((user, i) => (
                                            <MetricsTableRow
                                                key={user.user_id}
                                                grid={USERS_GRID}
                                                isLast={i === data.users.length - 1}
                                                onClick={() => openUser(user)}
                                            >
                                                <span style={{ overflow: "hidden", textOverflow: "ellipsis", whiteSpace: "nowrap" }}>
                                                    {trimId(user.user_id)}
                                                </span>
                                                <span style={{ textAlign: "right", fontVariantNumeric: "tabular-nums" }}>
                                                    {formatTableAmount(user.balance)}
                                                </span>
                                                <span style={{ fontSize: "0.8rem", color: "var(--ion-color-medium)" }}>
                                                    {user.app_users.length} app user{user.app_users.length === 1 ? "" : "s"}
                                                    {user.owner_of_app_id ? ` · owns ${trimId(user.owner_of_app_id)}` : ""}
                                                </span>
                                                <div style={{ display: "flex", justifyContent: "center" }}>
                                                    {user.locked ? (
                                                        <IonBadge color="danger">Locked</IonBadge>
                                                    ) : (
                                                        <IonText color="medium" style={{ fontSize: "0.8rem" }}>—</IonText>
                                                    )}
                                                </div>
                                            </MetricsTableRow>
                                        ))}
                                    </MetricsDataTable>
                                )}

                                <div style={{ display: "flex", gap: 8, marginTop: 12 }}>
                                    <IonButton fill="outline" disabled={!hasPrev || loading} onClick={() => setPage((p) => p - 1)}>
                                        Previous
                                    </IonButton>
                                    <IonButton fill="outline" disabled={!hasNext || loading} onClick={() => setPage((p) => p + 1)}>
                                        Next
                                    </IonButton>
                                    <IonButton fill="outline" disabled={loading} onClick={() => void fetchUsers()}>
                                        Refresh
                                    </IonButton>
                                </div>
                            </IonCardContent>
                        </IonCard>
                    </>
                )}
            </IonContent>
        </IonPage>
    );
}
