import { useCallback, useEffect, useMemo, useState } from "react";
import { RouteComponentProps } from "react-router-dom";
import {
    IonBadge,
    IonButton,
    IonCard,
    IonCardContent,
    IonCardHeader,
    IonCardTitle,
    IonContent,
    IonHeader,
    IonPage,
    IonText,
    useIonLoading,
    useIonRouter,
} from "@ionic/react";
import { toast } from "react-toastify";
import { UserAdminInfo, UserOperation } from "@/Api/pub/autogenerated/ts/types";
import MetricsSubPageToolbar from "@/Layout2/Metrics/MetricsSubPageToolbar";
import { useAppSelector } from "@/State/store/hooks";
import { selectAdminNprofileViews } from "@/State/scoped/backups/sources/selectors";
import { selectSelectedMetricsAdminSourceId } from "@/State/runtime/slice";
import { emptyCursor, parseOperationsResponse } from "@/State/scoped/backups/sources/history/helpers";
import type { HistoryCursor } from "@/State/scoped/backups/sources/history/types";
import { AdminOperationsList } from "./adminOperationDisplay";
import { fetcher } from "./fetcher";
import { formatTableAmount } from "./metricsDataTable";

type LocationState = { user?: UserAdminInfo };

function formatSats(n: number): string {
    return formatTableAmount(n) + " sats";
}

function trimId(id: string): string {
    return id.length <= 12 ? id : `${id.slice(0, 6)}…${id.slice(-4)}`;
}

export default function UserOperationsAdmin({ match, location, history }: RouteComponentProps) {
    const router = useIonRouter();
    const { userId } = match.params as { userId: string };
    const userFromState = (location.state as LocationState | undefined)?.user;

    const admins = useAppSelector(selectAdminNprofileViews);
    const selectedId = useAppSelector(selectSelectedMetricsAdminSourceId);
    const adminSource = useMemo(
        () => admins.find((a) => a.sourceId === selectedId),
        [admins, selectedId]
    );

    const [operations, setOperations] = useState<UserOperation[]>([]);
    const [cursor, setCursor] = useState<HistoryCursor>(emptyCursor);
    const [hasMore, setHasMore] = useState(false);
    const [loading, setLoading] = useState(true);
    const [loadingMore, setLoadingMore] = useState(false);
    const [error, setError] = useState<string | null>(null);
    const [presentLoading, dismissLoading] = useIonLoading();

    const fetchOperations = useCallback(async (nextCursor: HistoryCursor, append: boolean) => {
        if (!adminSource) return;

        const res = await fetcher(
            { pubkey: adminSource.lpk, relays: adminSource.relays },
            adminSource.keys,
            {
                onStart: async () => {
                    setError(null);
                    if (append) {
                        setLoadingMore(true);
                    } else {
                        setLoading(true);
                        await dismissLoading();
                        await presentLoading("Fetching operations...");
                    }
                },
                onEnd: async () => {
                    if (append) {
                        setLoadingMore(false);
                    } else {
                        setLoading(false);
                        await dismissLoading();
                    }
                },
                onFail: (err) => {
                    setError(err);
                    toast.error(err);
                },
            },
            [(client) => client.GetUserOperationsFromAdmin({ ...nextCursor, user_id: userId })]
        );

        if (!res) return;

        const { operations: batch, newCursor, needMoreData } = parseOperationsResponse(res[0], nextCursor);
        setOperations((prev) => (append ? [...prev, ...batch] : batch));
        setCursor(newCursor);
        setHasMore(needMoreData);
    }, [adminSource, dismissLoading, presentLoading, userId]);

    useEffect(() => {
        setOperations([]);
        setCursor(emptyCursor());
        setHasMore(false);
        void fetchOperations(emptyCursor(), false);
    }, [fetchOperations, userId]);

    const loadMore = () => {
        if (loadingMore || !hasMore) return;
        void fetchOperations(cursor, true);
    };

    return (
        <IonPage className="ion-page-width">
            <IonHeader className="ion-no-border">
                <MetricsSubPageToolbar title={`User ${trimId(userId)}`} backHref="/metrics/users" />
            </IonHeader>

            <IonContent className="ion-padding ion-content-no-footer">
                {error && (
                    <div style={{ color: "red", padding: 12 }}>
                        <div style={{ fontWeight: 700, marginBottom: 8 }}>Something went wrong</div>
                        <div style={{ opacity: 0.85, marginBottom: 12 }}>{error}</div>
                        <div style={{ display: "flex", gap: 8 }}>
                            <IonButton onClick={() => void fetchOperations(emptyCursor(), false)}>Retry</IonButton>
                            <IonButton fill="outline" onClick={() => history.push("/metrics/users")}>
                                Back to users
                            </IonButton>
                            <IonButton fill="outline" onClick={() => router.push("/metrics/select", "back")}>
                                Change Source
                            </IonButton>
                        </div>
                    </div>
                )}

                {!error && (
                    <>
                        <IonCard>
                            <IonCardHeader>
                                <IonCardTitle>{trimId(userId)}</IonCardTitle>
                            </IonCardHeader>
                            <IonCardContent>
                                {userFromState ? (
                                    <div
                                        style={{
                                            padding: "12px",
                                            background: "var(--ion-color-light)",
                                            borderRadius: "8px",
                                            display: "flex",
                                            flexDirection: "column",
                                            gap: 8,
                                        }}
                                    >
                                        <div style={{ display: "flex", justifyContent: "space-between" }}>
                                            <IonText color="medium">Balance</IonText>
                                            <IonText><strong>{formatSats(userFromState.balance)}</strong></IonText>
                                        </div>
                                        {userFromState.locked && (
                                            <div style={{ display: "flex", justifyContent: "space-between", alignItems: "center" }}>
                                                <IonText color="medium">Status</IonText>
                                                <IonBadge color="danger">Locked</IonBadge>
                                            </div>
                                        )}
                                        {userFromState.owner_of_app_id && (
                                            <div style={{ display: "flex", justifyContent: "space-between", gap: 8 }}>
                                                <IonText color="medium">App owner</IonText>
                                                <IonText style={{ textAlign: "right" }}>{trimId(userFromState.owner_of_app_id)}</IonText>
                                            </div>
                                        )}
                                        {userFromState.app_users.length > 0 && (
                                            <div style={{ display: "flex", justifyContent: "space-between" }}>
                                                <IonText color="medium">Linked app users</IonText>
                                                <IonText>{userFromState.app_users.length}</IonText>
                                            </div>
                                        )}
                                    </div>
                                ) : (
                                    <IonText color="medium">User summary unavailable — open from the users list for details.</IonText>
                                )}
                            </IonCardContent>
                        </IonCard>

                        <IonCard className="ion-margin-top">
                            <IonCardHeader>
                                <IonCardTitle>
                                    Operations
                                    {operations.length > 0 && (
                                        <IonText color="medium" style={{ fontSize: "0.85rem", fontWeight: 400, marginLeft: "8px" }}>
                                            ({operations.length})
                                        </IonText>
                                    )}
                                </IonCardTitle>
                            </IonCardHeader>
                            <IonCardContent>
                                {!loading && operations.length > 0 && (
                                    <IonText color="medium" style={{ fontSize: "0.8rem", display: "block", marginBottom: "12px" }}>
                                        {operations.length} operation{operations.length === 1 ? "" : "s"} loaded
                                    </IonText>
                                )}

                                {!loading && <AdminOperationsList operations={operations} />}

                                {hasMore && (
                                    <IonButton
                                        expand="block"
                                        fill="outline"
                                        className="ion-margin-top"
                                        disabled={loadingMore}
                                        onClick={loadMore}
                                    >
                                        {loadingMore ? "Loading..." : "Load more operations"}
                                    </IonButton>
                                )}
                            </IonCardContent>
                        </IonCard>
                    </>
                )}
            </IonContent>
        </IonPage>
    );
}
