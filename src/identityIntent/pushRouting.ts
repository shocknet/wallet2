import {
	PushNotificationPayload,
	PushNotificationPayloadValidate,
	PushNotificationPayload_data_type,
} from "@/Api/pub/autogenerated/ts/types";
import { nip44 } from "nostr-tools";
import { hexToBytes } from "@noble/hashes/utils";

export function decryptPushPayload(args: {
	privateKey: string;
	appNpubHex: string;
	encryptedPayload: string;
}): PushNotificationPayload | null {
	try {
		const sharedSecret = nip44.getConversationKey(
			hexToBytes(args.privateKey),
			args.appNpubHex
		);
		const plaintext = nip44.decrypt(args.encryptedPayload, sharedSecret);
		const parsed = JSON.parse(plaintext) as PushNotificationPayload;
		const validationError = PushNotificationPayloadValidate(parsed);
		if (validationError) return null;
		return parsed;
	} catch {
		return null;
	}
}

export function toPushRouteIntent(args: {
	sourceId: string;
	payload: PushNotificationPayload;
}): { path: string; state?: unknown } {
	if (args.payload.data.type === PushNotificationPayload_data_type.RECEIVED_OPERATION) {
		const operationId = args.payload.data.received_operation.operationId;
		if (operationId) {
			return {
				path: "/home",
				state: { notif_op_id: operationId, sourceId: args.sourceId },
			};
		}
	}
	if (args.payload.data.type === PushNotificationPayload_data_type.SENT_OPERATION) {
		const operationId = args.payload.data.sent_operation.operationId;
		if (operationId) {
			return {
				path: "/home",
				state: { notif_op_id: operationId, sourceId: args.sourceId },
			};
		}
	}
	return { path: "/home" };
}

