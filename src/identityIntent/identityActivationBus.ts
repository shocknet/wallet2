import dLogger from "@/Api/helpers/debugLog";
import { PushNotificationEnvelope, PushNotificationEnvelopeValidate } from "@/Api/pub/autogenerated/ts/types";


export type IdentityActivationEvent = {
	createdAtMs: number;
	identityId?: string;
	reason: "push-notification" | "resume-last-active" | "manual";
};

const _log = dLogger.withContext({ component: "identity-activation-bus" });

export interface IdentityIntent {
	createdAtMs: number;
	identityId: string;
	push?: {
		topicId: string;
		sourceId: string;
		envelope: PushNotificationEnvelope;
	}
}

function assertIdentityIntent(candidate: unknown): asserts candidate is IdentityIntent {
	if (!candidate || typeof candidate !== "object") {
		throw new Error("Invalid identity activation intent: expected object");
	}
	const maybeIntent = candidate as Partial<IdentityIntent>;
	if (typeof maybeIntent.createdAtMs !== "number") {
		throw new Error("Invalid identity activation intent: createdAtMs must be a number");
	}

	if (typeof maybeIntent.identityId !== "string") {
		throw new Error("Invalid identity activation intent: identityId must be a string");
	}
	if (maybeIntent.push) {
		if (typeof maybeIntent.push.topicId !== "string") {
			throw new Error("Invalid identity activation intent: push.topicId must be a string");
		}
		if (typeof maybeIntent.push.sourceId !== "string") {
			throw new Error("Invalid identity activation intent: push.sourceId must be a string");
		}
		if (!maybeIntent.push.envelope) {
			throw new Error("Invalid identity activation intent: push.envelope is required");
		}
		const err = PushNotificationEnvelopeValidate(maybeIntent.push.envelope);
		if (err) {
			throw new Error(`Invalid identity activation intent: push.envelope is invalid: ${err}`);
		}
	}
}

let pending: IdentityIntent | null = null;
let ready = false;
const listeners = new Set<(i: IdentityIntent) => void>();

const SS_KEY = "PUSH_CLICK_INTENT";



export function setIdentityIntent(i: IdentityIntent) {
	pending = i;
	try { sessionStorage.setItem(SS_KEY, JSON.stringify(i)); } catch {/*  */ }
	if (ready) listeners.forEach(fn => fn(i));
}

export function getIdentityIntent(): IdentityIntent | null {
	if (pending) return pending;
	try {
		const s = sessionStorage.getItem(SS_KEY);
		if (!s) return null;
		const candidate = JSON.parse(s);
		assertIdentityIntent(candidate);
		pending = candidate;
		return pending;
	} catch {
		return null;
	}
}

export function markIdentityIntentReady() {
	ready = true;
	const i = getIdentityIntent();
	if (i) listeners.forEach(fn => fn(i));
}

export function onIdentityIntent(fn: (i: IdentityIntent) => void) {
	listeners.add(fn);
	return () => {
		listeners.delete(fn)
	}
}

export function clearIdenityIntent() {
	pending = null;
	try { sessionStorage.removeItem(SS_KEY); } catch {/*  */ }
}
