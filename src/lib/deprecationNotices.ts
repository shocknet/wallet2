import { toast } from "react-toastify";
import { tsFilesWrapper } from "@/Components/Toast";
import { Preferences } from "@capacitor/preferences";
import { DeprecationNotice } from "@/Api/keylink/autogenerated/ts/types";



const STORAGE_KEY = "deprecation_notice_lastShownMs";
const COOLDOWN_MS = 2 * 60 * 60 * 1000; // 2 hours

const safeNow = () => Date.now();

const readLastShownMs = async () => {
	try {
		const { value: raw } = await Preferences.get({ key: STORAGE_KEY });
		if (!raw) return null;
		const n = Number(raw);
		return Number.isFinite(n) ? n : null;
	} catch {
		return null;
	}
};

const writeLastShownMs = async (ms: number) => {
	try {
		await Preferences.set({ key: STORAGE_KEY, value: String(ms) });
	} catch {
		// ignore storage failures
	}
};

export const maybeShowDeprecationNotice = async (notice: DeprecationNotice) => {
	if (!notice?.message) return;

	const now = safeNow();
	const last = await readLastShownMs();
	if (last !== null && now - last < COOLDOWN_MS) return;

	await writeLastShownMs(now);
	toast.warn(tsFilesWrapper({ title: "Deprecation notice", message: notice.message }));
};

